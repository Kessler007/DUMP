public class Account {
    Integer ID;
    String Nombre;
    Integer Number_Phone;
    String Email;
    String Password;

    public Account(Integer ID, String Nombre, Integer Number_Phone, String Email, String Password) {
        this.ID = ID;
        this.Nombre = Nombre;
        this.Number_Phone = Number_Phone;
        this.Email = Email;
        this.Password = Password;
    }
}
